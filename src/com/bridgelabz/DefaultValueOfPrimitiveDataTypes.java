package com.bridgelabz;

public class DefaultValueOfPrimitiveDataTypes
{
    //Default value of all primitive dataTypes.
    static int staticVar1;
    static byte staticVar2;
    static short staticVar3;
    static long staticVar4;
    static float staticVar5;
    static double staticVar6;
    static char staticVar7;
    static boolean staticVar8;
    public static void main(String[] args)
    {
        System.out.println("a static->" +staticVar1);
        System.out.println("b static ->" +staticVar2);
        System.out.println("c static ->" +staticVar3);
        System.out.println("d static ->" +staticVar4);
        System.out.println("e static ->" +staticVar5);
        System.out.println("f static ->" +staticVar6);
        System.out.println("g static ->" +staticVar7);
        System.out.println("h static ->" +staticVar8);
    }
}
